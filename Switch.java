public class Switch {
    static void switch_method() //switch method  in java
    {
        int happy=3;
       switch(happy)
       {
           case 1:System.out.println("happy coding 1");
           break;
           case 2:System.out.println("happpy coding 2");
           break;
           case 3:System.out.println("happy coding 3");
           break;
       }
    }

    public static void main(String[] args) {
        switch_method();
    }
    
}